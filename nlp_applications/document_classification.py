class LSTMClassifier:
    def __init__(self, vocab_size: int, embed_dim: int, hidden_dim: int,
                 num_classes: int, seed: int = 42) -> None:
        rng = np.random.default_rng(seed)
        self.E = rng.standard_normal((vocab_size, embed_dim)) * 0.01
        self.lstm = LSTMCell(embed_dim, hidden_dim, seed=seed)
        limit = np.sqrt(6.0 / (hidden_dim + num_classes))
        self.Wout = rng.uniform(-limit, limit, (num_classes, hidden_dim))
        self.bout = np.zeros(num_classes)
        self.H = hidden_dim

        # Adam for embedding / output
        self.mE   = np.zeros_like(self.E)
        self.vE   = np.zeros_like(self.E)
        self.mWo  = np.zeros_like(self.Wout)
        self.vWo  = np.zeros_like(self.Wout)
        self.mbo  = np.zeros_like(self.bout)
        self.vbo  = np.zeros_like(self.bout)
        self.t_out = 0

    def forward(self, seq: np.ndarray) -> np.ndarray:
        """seq: (T,) int token ids → probs (num_classes,).
        Uses mean-pooling of all hidden states for stable training."""
        T = seq.shape[0]
        h = np.zeros(self.H)
        c = np.zeros(self.H)
        self._embeds = []
        self._all_h  = []
        for t in range(T):
            emb = self.E[seq[t]]
            self._embeds.append((seq[t], emb))
            h, c = self.lstm.forward(emb, h, c)
            self._all_h.append(h.copy())

        # Mean pool over non-padding steps (seq[t] != 0)
        valid = np.array([seq[t] != 0 for t in range(T)], dtype=np.float64)
        n_valid = max(valid.sum(), 1.0)
        h_pool = sum(self._all_h[t] * valid[t] for t in range(T)) / n_valid

        logits = self.Wout @ h_pool + self.bout
        e = np.exp(logits - logits.max())
        probs = e / e.sum()
        self._last_h = h_pool
        self._last_h_last = h          # last actual hidden state (for compatibility)
        self._last_logits = logits
        self._probs = probs
        self._seq = seq
        self._valid = valid
        self._n_valid = n_valid
        return probs

    def backward(self, label: int) -> float:
        probs = self._probs
        loss  = -np.log(max(probs[label], 1e-9))

        dlogits = probs.copy()
        dlogits[label] -= 1.0

        h_pool = self._last_h
        self.dWout = np.outer(dlogits, h_pool)
        self.dbout = dlogits
        dh_pool = self.Wout.T @ dlogits    # (H,)

        # Distribute mean-pool gradient to each valid step
        T      = len(self._embeds)
        valid  = self._valid
        n_val  = self._n_valid
        dc = np.zeros_like(dh_pool)
        self.dE_updates: list[tuple[int, np.ndarray]] = []

        # TBPTT: only backprop through last BPTT_STEPS steps for stability
        BPTT = min(T, 20)
        dh = np.zeros_like(dh_pool)
        for t in reversed(range(T)):
            if valid[t]:
                dh = dh + dh_pool / n_val      # add pooled gradient
            if t >= T - BPTT:
                dx, dh, dc = self.lstm.backward(dh, dc)
                # Clip gradients to prevent explosion
                dh = np.clip(dh, -1.0, 1.0)
                dc = np.clip(dc, -1.0, 1.0)
                tok_id = self._embeds[t][0]
                self.dE_updates.append((tok_id, np.clip(dx, -1.0, 1.0)))
            else:
                # Skip backprop but still track token for embedding update
                tok_id = self._embeds[t][0]
                self.dE_updates.append((tok_id, np.zeros_like(dh_pool[:self.E.shape[1]] if self.E.shape[1] < len(dh_pool) else dh_pool)))

        return loss

    def step(self, lr: float) -> None:
        self.lstm.adam_update(lr)

        self.t_out += 1
        b1, b2, eps = 0.9, 0.999, 1e-8
        for param, grad, m, v in [
            (self.Wout, self.dWout, self.mWo, self.vWo),
            (self.bout, self.dbout, self.mbo, self.vbo),
        ]:
            m[:] = b1 * m + (1 - b1) * grad
            v[:] = b2 * v + (1 - b2) * grad ** 2
            mh = m / (1 - b1 ** self.t_out)
            vh = v / (1 - b2 ** self.t_out)
            param -= lr * mh / (np.sqrt(vh) + eps)

        # Sparse embedding update
        for tok_id, dx in self.dE_updates:
            self.mE[tok_id] = b1 * self.mE[tok_id] + (1 - b1) * dx
            self.vE[tok_id] = b2 * self.vE[tok_id] + (1 - b2) * dx ** 2
            mh = self.mE[tok_id] / (1 - b1 ** self.t_out)
            vh = self.vE[tok_id] / (1 - b2 ** self.t_out)
            self.E[tok_id] -= lr * mh / (np.sqrt(vh) + eps)

    def predict(self, seq: np.ndarray) -> int:
        return int(self.forward(seq).argmax())


