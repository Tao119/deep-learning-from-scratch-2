# Confusion matrix & F1
# ================================================================== #

def confusion_matrix(preds: list[int], labels: list[int], n_cls: int) -> np.ndarray:
    cm = np.zeros((n_cls, n_cls), dtype=np.int64)
    for p, g in zip(preds, labels):
        cm[g, p] += 1
    return cm


def per_class_f1(cm: np.ndarray) -> list[float]:
    f1s = []
    for c in range(cm.shape[0]):
        tp = cm[c, c]
        fp = cm[:, c].sum() - tp
        fn = cm[c, :].sum() - tp
        prec = tp / max(tp + fp, 1)
        rec  = tp / max(tp + fn, 1)
        f1   = 2 * prec * rec / max(prec + rec, 1e-9)
        f1s.append(f1)
    return f1s


