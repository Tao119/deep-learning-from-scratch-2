class LSTMCell:
    """Single LSTM cell: manual forward/backward."""

    def __init__(self, input_dim: int, hidden_dim: int, seed: int = 0) -> None:
        rng = np.random.default_rng(seed)
        D, H = input_dim, hidden_dim
        # Concatenated weight matrix [W_i | W_f | W_g | W_o]
        scale = np.sqrt(1.0 / (D + H))
        self.W = rng.uniform(-scale, scale, (4 * H, D + H))
        self.b = np.zeros(4 * H)
        self.H = H
        # Adam
        self.mW = np.zeros_like(self.W)
        self.vW = np.zeros_like(self.W)
        self.mb = np.zeros_like(self.b)
        self.vb = np.zeros_like(self.b)
        self.t = 0

    def _gates(self, x: np.ndarray, h: np.ndarray) -> tuple:
        H = self.H
        xh = np.concatenate([x, h])
        raw = self.W @ xh + self.b
        i = _sigmoid(raw[      :   H])
        f = _sigmoid(raw[  H   : 2*H])
        g = np.tanh (raw[2*H   : 3*H])
        o = _sigmoid(raw[3*H   :    ])
        return i, f, g, o, xh, raw

    def forward(self, x: np.ndarray, h: np.ndarray, c: np.ndarray
                ) -> tuple[np.ndarray, np.ndarray]:
        i, f, g, o, xh, raw = self._gates(x, h)
        c_new = f * c + i * g
        h_new = o * np.tanh(c_new)
        self._cache = {"i": i, "f": f, "g": g, "o": o,
                       "xh": xh, "c": c, "c_new": c_new, "h": h, "raw": raw}
        return h_new, c_new

    def backward(self, dh_next: np.ndarray, dc_next: np.ndarray,
                 ) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
        H = self.H
        i, f, g, o = self._cache["i"], self._cache["f"], self._cache["g"], self._cache["o"]
        c, c_new = self._cache["c"], self._cache["c_new"]
        xh = self._cache["xh"]
        raw = self._cache["raw"]

        tanh_c_new = np.tanh(c_new)
        dh = dh_next
        do = dh * tanh_c_new
        dtanh = dh * o
        dc = dtanh * (1.0 - tanh_c_new ** 2) + dc_next
        di = dc * g
        df = dc * c
        dg = dc * i
        dc_prev = dc * f

        di_raw = di * i * (1 - i)
        df_raw = df * f * (1 - f)
        dg_raw = dg * (1 - g ** 2)
        do_raw = do * o * (1 - o)

        draw = np.concatenate([di_raw, df_raw, dg_raw, do_raw])
        self.dW = np.outer(draw, xh)
        self.db = draw
        dxh = self.W.T @ draw
        dx   = dxh[:xh.shape[0] - H]
        dh_prev = dxh[xh.shape[0] - H:]
        return dx, dh_prev, dc_prev

    def adam_update(self, lr: float, beta1=0.9, beta2=0.999, eps=1e-8) -> None:
        self.t += 1
        for param, grad, m, v in [
            (self.W, self.dW, self.mW, self.vW),
            (self.b, self.db, self.mb, self.vb),
        ]:
            m[:] = beta1 * m + (1 - beta1) * grad
            v[:] = beta2 * v + (1 - beta2) * grad ** 2
            m_hat = m / (1 - beta1 ** self.t)
            v_hat = v / (1 - beta2 ** self.t)
            param -= lr * m_hat / (np.sqrt(v_hat) + eps)


def _sigmoid(x: np.ndarray) -> np.ndarray:
    return np.where(x >= 0, 1.0 / (1.0 + np.exp(-x)),
                    np.exp(x) / (1.0 + np.exp(x)))


