# ================================================================== #
# Character-level tokeniser
# ================================================================== #

class CharTokenizer:
    def __init__(self) -> None:
        self.char2idx: dict[str, int] = {"<PAD>": 0, "<UNK>": 1}
        self.idx2char: list[str] = ["<PAD>", "<UNK>"]

    def fit(self, texts: list[str]) -> None:
        for text in texts:
            for ch in text:
                if ch not in self.char2idx:
                    self.char2idx[ch] = len(self.char2idx)
                    self.idx2char.append(ch)

    def encode(self, text: str, max_len: int) -> np.ndarray:
        seq = [self.char2idx.get(ch, 1) for ch in text[:max_len]]
        if len(seq) < max_len:
            seq += [0] * (max_len - len(seq))
        return np.array(seq, dtype=np.int64)

    @property
    def vocab_size(self) -> int:
        return len(self.char2idx)


