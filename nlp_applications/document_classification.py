def main():
    print("=" * 60)
    print("LSTM Document Classification (character-level)")
    print("5 categories × 50 documents = 250 total")
    print("=" * 60)

    rng = np.random.default_rng(0)
    docs, labels = build_dataset()

    # Shuffle
    idx = rng.permutation(len(docs))
    docs   = [docs[i] for i in idx]
    labels = [labels[i] for i in idx]

    # Tokenise
    tokenizer = CharTokenizer()
    tokenizer.fit(docs)
    MAX_LEN = 60   # Focus on first 60 chars which contain the most discriminative signal
    seqs = np.stack([tokenizer.encode(d, MAX_LEN) for d in docs])

    # Train/test split: 80/20
    n_train = int(0.8 * len(docs))
    X_tr, y_tr = seqs[:n_train],  labels[:n_train]
    X_te, y_te = seqs[n_train:],  labels[n_train:]

    model = LSTMClassifier(
        vocab_size=tokenizer.vocab_size,
        embed_dim=32,
        hidden_dim=128,
        num_classes=5,
        seed=42,
    )

    EPOCHS = 100
    LR     = 0.003
    print(f"\nVocab size: {tokenizer.vocab_size} | Seq len: {MAX_LEN}")
    print(f"Train: {n_train} | Test: {len(docs) - n_train}\n")

    for ep in range(EPOCHS):
        order = rng.permutation(n_train)
        total_loss = 0.0
        for i in order:
            probs = model.forward(X_tr[i])
            loss  = model.backward(y_tr[i])
            model.step(LR)
            total_loss += loss

        if (ep + 1) % 10 == 0:
            preds_tr = [model.predict(X_tr[i]) for i in range(n_train)]
            acc_tr = sum(p == g for p, g in zip(preds_tr, y_tr)) / n_train
            preds_te = [model.predict(X_te[i]) for i in range(len(y_te))]
            acc_te = sum(p == g for p, g in zip(preds_te, y_te)) / len(y_te)
            print(f"Epoch {ep+1:3d}/{EPOCHS}  loss={total_loss/n_train:.4f}  "
                  f"train_acc={acc_tr:.3f}  test_acc={acc_te:.3f}")

    # Final evaluation
    print("\n--- Final Evaluation ---")
    preds_te = [model.predict(X_te[i]) for i in range(len(y_te))]
    acc = sum(p == g for p, g in zip(preds_te, y_te)) / len(y_te)
    print(f"Test Accuracy: {acc:.3f}")

    cm = confusion_matrix(preds_te, y_te, n_cls=5)
    f1s = per_class_f1(cm)
    print("\nConfusion Matrix (rows=true, cols=pred):")
    header = "      " + "  ".join(f"{c[:3]:>4}" for c in CATEGORIES)
    print(header)
    for c, row in zip(CATEGORIES, cm):
        print(f"  {c[:3]:>4}  " + "  ".join(f"{v:4d}" for v in row))

    print("\nPer-class F1:")
    for cat, f1 in zip(CATEGORIES, f1s):
        print(f"  {cat:6s}: {f1:.3f}")
    print(f"\nMacro F1: {np.mean(f1s):.3f}")

    # Demo: classify 5 new documents
    print("\n--- Demo: Classify New Documents ---")
    demos = [
        ("量子力学の重ね合わせ原理は電子が同時に複数の状態を取ることを示す。", "科学"),
        ("国会では来年度の予算案について与野党が激しく論戦を交わしている。", "政治"),
        ("昨日の試合で選手は3点差をひっくり返す劇的な逆転勝利を収めた。", "スポーツ"),
        ("ピアニストは繊細なタッチで夜想曲の旋律を情感豊かに奏でた。", "音楽"),
        ("玉ねぎをきつね色になるまで炒めてからトマトソースを加える。", "料理"),
    ]
    for text, true_cat in demos:
        seq  = tokenizer.encode(text, MAX_LEN)
        pred = model.predict(seq)
        print(f"  Text: {text[:25]}...")
        print(f"  True: {true_cat:6s}  Pred: {CATEGORIES[pred]}")


if __name__ == "__main__":
    main()
